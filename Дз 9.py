# Число Армстронга — це число, яке дорівнює сумі своїх цифр, піднесених до степеня, що дорівнює кількості цифр.
# 153 = 1^3 + 5^3 + 3^3
# n = кількість розрядів
# n = 153


n = int(input("Введіть число: "))

s = str(n)
k = len(s)
t = 0
f = True
for d in s:
    if not f:
        print(" + ", end="")
    print(f"{d}^{k}", end="")
    t += int(d) ** k
    f = False

print(" =", t)


